package July2025.ex02072025.Operators;

public class arithmatic_operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        String s1 = "Suraj";
        String s2 = "Dubey";
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println(a+b+ " "+s1+s2);

        System.out.println(s1+s2+ " "+a+b); // in this case a and b value working as a string

    }
}
