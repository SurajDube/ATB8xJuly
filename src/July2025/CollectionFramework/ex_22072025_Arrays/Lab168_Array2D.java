package July2025.CollectionFramework.ex_22072025_Arrays;

public class Lab168_Array2D {
    public static void main(String[] args) {
        int[][] matrix = {   {77,85,68,99,87},  // 3 row and 5 column
                            {98,56,79,90,92},   //3*5 array
                            {78,88,56,70,99}            };
        System.out.println("row.length: " +matrix.length);   // it will print length of rows.
        System.out.println("column.length: " +matrix[0].length);   // it will print length of columns.
    }
}
